# Mathematical Operators

print("Addition: " + str(3 + 5))
print("Subtraction: " + str(5 - 3))
print("Multiplication: " + str(3 * 5))
print("Divsion: " + str(4 / 2))
print("Modulus: " + str(5 % 3))
print("Exponentiation: " + str(2 ** 3))
print("Floor Division: " + str(5 // 2))

# Rounding

print(8 / 3) # 2.6666666666666665
print(round(8 / 3, 2)) # Rounds to 2 decimal places 2.67

