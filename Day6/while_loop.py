# While Loop

number_of_hurdles = 6
while number_of_hurdles > 0:
    print("Jumped")
    number_of_hurdles -= 1
    print(number_of_hurdles)

# For loop great for iterating over things and 
# need to do something with each thing you iterate over.

# While loop great for when you don't care what number
# in a sequence you're in. Just want to do something
# until a condition is met. Dangerous! Infinite loops.